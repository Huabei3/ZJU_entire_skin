% ===================================================
% *** FUNCTION graybackground
% *** 这是计算均匀灰色背景肤色图像的最佳白点模型
% *** 输入cct表示环境光的相关色温，是一个数值
% *** 输出CCT表示对应光环境下肤色图像的最佳白点的相关色温
% *** 输出Duv表示对应光环境下肤色图像的最佳白点的Duv
% ===================================================
function [CCT,Duv]=graybackground(cct)
c1 = -1.561e-05;
c2 = 0.1952;
c3 = 6633;
CCT= c1*cct^2+c2*cct+c3;
d1 = -1.152e-10;
d2 = 1.462e-06;
d3 = -0.008381;
Duv=d1*cct^2+d2*cct+d3;
end


