% ===================================================
% *** FUNCTION realbackground
% *** 这是计算真实背景肤色图像的最佳白点模型
% *** 输入cct表示环境光的相关色温，是一个数值
% *** 输出CCT表示对应光环境下肤色图像的最佳白点的相关色温
% *** 输出Duv表示对应光环境下肤色图像的最佳白点的Duv
% ===================================================
function [CCT,Duv]=realbackground(cct)
c1 = -0.0001707;
c2 = 1.886;
c3 = 928.3;
CCT= c1*cct^2+c2*cct+c3;

d1 =  5.424e-12;
d2 = -1.622e-07;
d3 = -0.0008486;
Duv=d1*cct^2+d2*cct+d3;
end